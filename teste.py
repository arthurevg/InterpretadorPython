# Teste simples do interpretador
x = 10
y = 3.5
nome = "Teste"
print("seu nome é: ", nome)

print("Valores iniciais:")
print(x)
print(y)
print(nome)

# Operações aritméticas
soma = x + y
multiplicacao = x * 2
divisao = x / 2

print("Resultados das operações:")
print(soma)
print(multiplicacao)
print(divisao)

# Condicional
if x > 5:
    print("x é maior que 5")
else:
    print("x é menor ou igual a 5")

# Loop simples
contador = 0
while contador < 3:
    print("Contador:", contador)
    contador = contador + 1

print("Fim do teste!")